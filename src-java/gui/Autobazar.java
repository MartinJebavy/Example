    private void jButtonUmytoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUmytoActionPerformed
        UmyjOsobniAuta osAuta = new UmyjOsobniAuta(seznam);
        UmyjNakladniAuta naklAuto = new UmyjNakladniAuta(seznam);
        try {
            if (frontaOS != null) {
                osAuta.nastavCasMyti(seznam, frontaOS);
            }
            if (frontaNak != null) {
                naklAuto.nastavCasMyti(seznam, frontaNak);
            }

            JOptionPane.showMessageDialog(this, "Vybraná auta byla umyta.");
            jTextAreaVystup.setText("");
            frontaNak = null;
            frontaOS = null;
        } catch (ISeznamAutomobilu.AutomobilSeznamException | ParseException ex) {
            Logger.getLogger(Autobazar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonUmytoActionPerformed

    private void jButtonUmyjOsAutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUmyjOsAutaActionPerformed
        if (seznam != null) {

            String text = "";
            if (frontaNak != null) {
                text += jTextAreaVystup.getText();
            }
            UmyjOsobniAuta osAuta = new UmyjOsobniAuta(seznam);
            frontaOS = osAuta.vratFrontu();
            text += "FRONTA NA UMITÍ OSOBNÍCH AUT\n\n";
            text += "TYP\t\tROK VYROBY\t\tSPZ\tSTAV TACHOMETRU [km]\tDATUM MYTÍ\t\tDELKA [m]\tBARVA\n\n";
            for (int i = 0; i < frontaOS.length; i++) {
                OsobniAutomobil os = (OsobniAutomobil) seznam.vratAuto(frontaOS, i);
                text += os.toString();

            }
            text += "\n\n";
            jTextAreaVystup.setText(text);
        }
    }//GEN-LAST:event_jButtonUmyjOsAutaActionPerformed

    private void jButtonUmyjNaklAutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUmyjNaklAutaActionPerformed
        if (seznam != null) {
            String text = "";
            if (frontaOS != null) {
                text += jTextAreaVystup.getText();
            }
            UmyjNakladniAuta osNakl = new UmyjNakladniAuta(seznam);
            frontaNak = osNakl.vratFrontu();
            text += "FRONTA NA UMITÍ NÁKLADNÍCH AUT\n\n";
            text += "TYP\t\tROK VYROBY\t\tSPZ\tSTAV TACHOMETRU [km]\tDATUM MYTÍ\t\tDELKA [m]\tBARVA\n\n";
            for (int i = 0; i < frontaNak.length; i++) {
                NakladniAutomobil nakl = (NakladniAutomobil) seznam.vratAuto(frontaNak, i);
                text += nakl.toString();
            }
            text += "\nCelková délka na umití za pracovní dobu 8h(1m/5minut): " + osNakl.getDelka() + "\n\n\n";

            jTextAreaVystup.setText(text);
        }
    }//GEN-LAST:event_jButtonUmyjNaklAutaActionPerformed

