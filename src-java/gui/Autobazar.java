    private void jButtonVypisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVypisActionPerformed
        if (seznam != null) {
            VypisSeznam dialog = new VypisSeznam(this, true);
            dialog.setVisible(true);
            IAutomobil[] poleAut = seznam.getPoleAutomobilu();
            String text = "";
            text = "TYP\t\tROK VYROBY\t\tSPZ\tSTAV TACHOMETRU [km]\tDATUM MYTÍ\t\tDELKA [m]\tBARVA\n\n";
            for (int i = 0; i < poleAut.length; i++) {

                if (poleAut[i] instanceof OsobniAutomobil && dialog.isPravdaOsAuta() == true) {
                    OsobniAutomobil os = (OsobniAutomobil) seznam.vratAuto(poleAut, i);
                    text += os.toString();
                }
                if (poleAut[i] instanceof NakladniAutomobil && dialog.isPravdaNaklAuta() == true) {
                    NakladniAutomobil nakl = (NakladniAutomobil) seznam.vratAuto(poleAut, i);
                    text += nakl.toString();
                }

            }
            jTextAreaVystup.setText(text);
        } else {
            JOptionPane.showMessageDialog(this, "Seznam je prazdný");
        }
    }//GEN-LAST:event_jButtonVypisActionPerformed

