    private void jButtonVypisStavActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVypisStavActionPerformed
        if (seznam != null) {
            IAutomobil[] poleOsobnichAut = seznam.getPoleAutomobilu(TypAutomobilu.OSOBNI_AUTOMOBIL);
            IAutomobil[] poleNakladnichAut = seznam.getPoleAutomobilu(TypAutomobilu.NAKLADNI_AUTOMOBIL);
            String text = "";
            text += "Celkový počet aut v seznamu:\t" + (poleOsobnichAut.length + poleNakladnichAut.length) + "\n\n";
            text += "Počet osobních automobilů:\t" + (poleOsobnichAut.length) + "\n";
            text += "Počet nákládních automobilů:\t" + (poleNakladnichAut.length) + "\n";
            jTextAreaVystup.setText(text);
        }else{
            jTextAreaVystup.setText("PRAZDNÝ SEZNAM");
        }
    }//GEN-LAST:event_jButtonVypisStavActionPerformed

