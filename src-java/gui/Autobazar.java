    private void jButtonNactiSeznamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNactiSeznamActionPerformed
        SouborCteni cti = new SouborCteni();
        String vypis = cti.cteniZeSouboru("seznam.txt");
        seznam = SeznamAut.getInstance();
        try {
            seznam.nactiSeznam(vypis);
            jTextAreaVystup.setText("");
        } catch (ISeznamAutomobilu.AutomobilSeznamException | ParseException ex) {
            Logger.getLogger(Autobazar.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Seznam se úspesně načetl");


    }//GEN-LAST:event_jButtonNactiSeznamActionPerformed

